"""
Rendering: LaTeX table fragments and figure generation.

All LaTeX output uses booktabs style.
Figures require matplotlib; if unavailable, figures are skipped and a
'figures_not_built.txt' is written.
"""

from __future__ import annotations

from pathlib import Path
from typing import Any

from .constants import (
    AXIS_COUNT,
    AXIS_ISI_KEYS,
    AXIS_SHORT_LABELS,
    AXIS_SLUGS,
    SCORE_DECIMALS_PAPER,
)
from .io_utils import write_text


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _fmt_score(val: float, decimals: int = SCORE_DECIMALS_PAPER) -> str:
    """Format a score to fixed decimal places."""
    return f"{val:.{decimals}f}"


def _classification_short(cls: str) -> str:
    """Shorten classification labels for tables."""
    mapping = {
        "highly_concentrated": "High",
        "moderately_concentrated": "Moderate",
        "mildly_concentrated": "Mild",
        "unconcentrated": "Low",
    }
    return mapping.get(cls, cls)


# ---------------------------------------------------------------------------
# Table: Full Ranking
# ---------------------------------------------------------------------------

def render_ranking_table(
    ranked: list[dict[str, Any]],
    output_path: Path,
) -> None:
    """
    Generate tables_tex/table_ranking_full.tex

    Columns: Rank | Country | ISI | Classification | A1 | A2 | A3 | A4 | A5 | A6
    Booktabs style.
    """
    lines: list[str] = []
    lines.append("% Auto-generated by ISI Results Evidence Pack. Do not edit.")
    lines.append("% Source: backend/snapshots/v1.0/2024/isi.json")
    lines.append(r"\begin{tabular}{rl" + "c" * (2 + AXIS_COUNT) + "}")
    lines.append(r"\toprule")

    # Header
    header_parts = [
        r"\textbf{Rank}",
        r"\textbf{Country}",
        r"\textbf{ISI}",
        r"\textbf{Class.}",
    ]
    for i in range(1, AXIS_COUNT + 1):
        header_parts.append(r"\textbf{" + AXIS_SHORT_LABELS[i] + "}")
    lines.append(" & ".join(header_parts) + r" \\")
    lines.append(r"\midrule")

    # Rows
    for r in ranked:
        row_parts = [
            str(r["rank"]),
            r["country_name"],
            _fmt_score(r["isi_composite"]),
            _classification_short(r["classification"]),
        ]
        for key in AXIS_ISI_KEYS:
            row_parts.append(_fmt_score(r[key]))
        lines.append(" & ".join(row_parts) + r" \\")

    lines.append(r"\bottomrule")
    lines.append(r"\end{tabular}")
    lines.append("")

    write_text(output_path, "\n".join(lines))


# ---------------------------------------------------------------------------
# Table: Summary Statistics
# ---------------------------------------------------------------------------

def render_summary_stats_table(
    stats: dict[str, Any],
    output_path: Path,
) -> None:
    """Generate tables_tex/table_summary_stats.tex"""
    lines: list[str] = []
    lines.append("% Auto-generated by ISI Results Evidence Pack. Do not edit.")
    lines.append(r"\begin{tabular}{lr}")
    lines.append(r"\toprule")
    lines.append(r"\textbf{Statistic} & \textbf{Value} \\")
    lines.append(r"\midrule")

    rows = [
        ("Countries (EU-27)", str(stats["n"])),
        ("Minimum", _fmt_score(stats["min"])),
        ("Maximum", _fmt_score(stats["max"])),
        ("Mean", _fmt_score(stats["mean"])),
        ("Median", _fmt_score(stats["median"])),
        (r"Std.\ Dev.\ ($\sigma$)", _fmt_score(stats["std_population"])),
        ("10th percentile", _fmt_score(stats["percentiles"]["p10"])),
        ("25th percentile", _fmt_score(stats["percentiles"]["p25"])),
        ("75th percentile", _fmt_score(stats["percentiles"]["p75"])),
        ("90th percentile", _fmt_score(stats["percentiles"]["p90"])),
    ]

    for label, value in rows:
        lines.append(f"{label} & {value}" + r" \\")

    lines.append(r"\bottomrule")
    lines.append(r"\end{tabular}")
    lines.append("")

    write_text(output_path, "\n".join(lines))


# ---------------------------------------------------------------------------
# Table: Axis Drivers (Top/Bottom 5)
# ---------------------------------------------------------------------------

def render_axis_drivers_table(
    drivers: list[dict[str, Any]],
    output_path: Path,
) -> None:
    """
    Generate table showing top 5 and bottom 5 countries by composite,
    with their primary axis driver.
    """
    lines: list[str] = []
    lines.append("% Auto-generated by ISI Results Evidence Pack. Do not edit.")
    lines.append(r"\begin{tabular}{rlcccc}")
    lines.append(r"\toprule")
    lines.append(
        r"\textbf{Rank} & \textbf{Country} & \textbf{ISI} & "
        r"\textbf{Max Axis} & \textbf{Max Score} & \textbf{Profile} \\"
    )
    lines.append(r"\midrule")

    # Top 5
    lines.append(r"\multicolumn{6}{l}{\textit{Most concentrated}} \\")
    for d in drivers[:5]:
        lines.append(
            f"{d['rank']} & {d['country_name']} & "
            f"{_fmt_score(d['isi_composite'])} & "
            f"{d['max_axis']} & {_fmt_score(d['max_axis_score'])} & "
            f"{d['profile_type']}" + r" \\"
        )

    lines.append(r"\midrule")
    lines.append(r"\multicolumn{6}{l}{\textit{Least concentrated}} \\")
    for d in drivers[-5:]:
        lines.append(
            f"{d['rank']} & {d['country_name']} & "
            f"{_fmt_score(d['isi_composite'])} & "
            f"{d['max_axis']} & {_fmt_score(d['max_axis_score'])} & "
            f"{d['profile_type']}" + r" \\"
        )

    lines.append(r"\bottomrule")
    lines.append(r"\end{tabular}")
    lines.append("")

    write_text(output_path, "\n".join(lines))


# ---------------------------------------------------------------------------
# Table: Correlation Matrix
# ---------------------------------------------------------------------------

def render_correlation_table(
    corr: dict[str, Any],
    output_path: Path,
) -> None:
    """Generate tables_tex/table_corr_matrix.tex"""
    axes = corr["axes"]
    matrix = corr["matrix"]
    n = len(axes)

    short_labels = [AXIS_SHORT_LABELS[i + 1] for i in range(n)]

    lines: list[str] = []
    lines.append("% Auto-generated by ISI Results Evidence Pack. Do not edit.")
    lines.append(r"\begin{tabular}{l" + "r" * n + "}")
    lines.append(r"\toprule")

    header = " & ".join([" "] + [r"\textbf{" + sl + "}" for sl in short_labels])
    lines.append(header + r" \\")
    lines.append(r"\midrule")

    for i in range(n):
        row_parts = [r"\textbf{" + short_labels[i] + "}"]
        for j in range(n):
            if i == j:
                row_parts.append("1.0000")
            else:
                row_parts.append(f"{matrix[i][j]:.4f}")
        lines.append(" & ".join(row_parts) + r" \\")

    lines.append(r"\bottomrule")
    lines.append(r"\end{tabular}")
    lines.append("")

    write_text(output_path, "\n".join(lines))


