def render_per_axis_stats_table(
    axis_stats: dict[str, dict[str, float]],
    output_path: Path,
) -> None:
    """Generate tables_tex/table_axis_stats.tex"""
    lines: list[str] = []
    lines.append("% Auto-generated by ISI Results Evidence Pack. Do not edit.")
    lines.append(r"\begin{tabular}{lrrrrr}")
    lines.append(r"\toprule")
    lines.append(
        r"\textbf{Axis} & \textbf{Min} & \textbf{Max} & "
        r"\textbf{Mean} & \textbf{Median} & \textbf{Std.\ Dev.} \\"
    )
    lines.append(r"\midrule")

    for i, key in enumerate(AXIS_ISI_KEYS):
        s = axis_stats[key]
        label = AXIS_SHORT_LABELS[i + 1]
        lines.append(
            f"{label} & {_fmt_score(s['min'])} & {_fmt_score(s['max'])} & "
            f"{_fmt_score(s['mean'])} & {_fmt_score(s['median'])} & "
            f"{_fmt_score(s['std_population'])}" + r" \\"
        )

    lines.append(r"\bottomrule")
    lines.append(r"\end{tabular}")
    lines.append("")

    write_text(output_path, "\n".join(lines))


# ---------------------------------------------------------------------------
# Figures
# ---------------------------------------------------------------------------

def try_render_figures(
    ranked: list[dict[str, Any]],
    corr: dict[str, Any],
    figures_dir: Path,
) -> bool:
    """
    Attempt to generate PDF figures using matplotlib.
    Returns True if successful, False if matplotlib is unavailable.
    """
    try:
        import matplotlib
        matplotlib.use("Agg")
        import matplotlib.pyplot as plt
    except ImportError:
        return False

    figures_dir.mkdir(parents=True, exist_ok=True)

    # --- Histogram of composite scores ---
    composites = [r["isi_composite"] for r in ranked]
    fig, ax = plt.subplots(figsize=(6, 4))
    ax.hist(composites, bins=min(15, len(composites)), color="#4a4a4a", edgecolor="white", linewidth=0.5)
    ax.set_xlabel("ISI Composite Score")
    ax.set_ylabel("Number of Countries")
    ax.set_title("Distribution of ISI Composite Scores (EU-27, 2024)")
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)
    fig.tight_layout()
    fig.savefig(figures_dir / "hist_composite.pdf", dpi=300)
    plt.close(fig)

    # --- Box plot of axis distributions ---
    axis_data = []
    axis_labels = []
    for i, key in enumerate(AXIS_ISI_KEYS):
        axis_data.append([r[key] for r in ranked])
        axis_labels.append(AXIS_SHORT_LABELS[i + 1])

    fig, ax = plt.subplots(figsize=(8, 5))
    bp = ax.boxplot(axis_data, labels=axis_labels, patch_artist=True)
    for patch in bp["boxes"]:
        patch.set_facecolor("#b0b0b0")
    ax.set_ylabel("Score")
    ax.set_title("Axis Score Distributions (EU-27, 2024)")
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)
    fig.tight_layout()
    fig.savefig(figures_dir / "boxplot_axes.pdf", dpi=300)
    plt.close(fig)

    # --- Correlation heatmap ---
    matrix = corr["matrix"]
    n = len(matrix)
    labels = [AXIS_SHORT_LABELS[i + 1] for i in range(n)]

    fig, ax = plt.subplots(figsize=(7, 6))
    im = ax.imshow(matrix, cmap="Greys", vmin=-1, vmax=1, aspect="equal")
    ax.set_xticks(range(n))
    ax.set_yticks(range(n))
    ax.set_xticklabels(labels, rotation=45, ha="right", fontsize=9)
    ax.set_yticklabels(labels, fontsize=9)
    for i_row in range(n):
        for j_col in range(n):
            val = matrix[i_row][j_col]
            color = "white" if abs(val) > 0.5 else "black"
            ax.text(j_col, i_row, f"{val:.2f}", ha="center", va="center",
                    fontsize=8, color=color)
    ax.set_title("Pearson Correlation Matrix — ISI Axes (EU-27, 2024)")
    fig.colorbar(im, ax=ax, shrink=0.8)
    fig.tight_layout()
    fig.savefig(figures_dir / "corr_heatmap.pdf", dpi=300)
    plt.close(fig)

    return True
